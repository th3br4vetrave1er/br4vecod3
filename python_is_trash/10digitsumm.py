# Подсчитать сумму 10 первых чисел.

x = 1
summa = 0
while x <= 10:
    summa += x
    x += 1
print(summa)

# Начинаем с 1, так как нужно считать сумму первых 10 чисел
# Переменная для хранения суммы
# Добавляем текущее значение x к сумме
# Увеличиваем x на 1, чтобы перейти к следующему числу
# Выводим итоговую сумму