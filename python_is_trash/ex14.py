# 3. С помощью вложенного списка эмитировать матрицу чисел 3x3. Значения
# заполняются случайным образом. Подсчитать сумму эдементов главной
# диагонали матрицы
import random

# Создаем матрицу 3x3 и заполняем случайными числами
matrix = [[random.randint(1, 10) for _ in range(3)] for _ in range(3)]

# Выводим матрицу для наглядности
print("Матрица:")
for row in matrix:
    print(row)

# Вычисляем сумму элементов на главной диагонали
sum_diagonal = sum(matrix[i][i] for i in range(3))

# Выводим сумму элементов на главной диагонали
print(f"Сумма элементов на главной диагонали: {sum_diagonal}")


